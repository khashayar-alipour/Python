'''   Bitwise operators assignment  '''
#======================================
# عملگرهای بیتی
# برای فهمشان باید اعداد را به صورت دودویی (Binary) ببینی
# هر رقم 0 یا 1 را یک بیت (Bit) می‌گویند
# مثلا عدد 5 میشه 101


#_________________
# bitwise AND 
# بیتی 1 می‌شود که هر دو بیت 1 باشند
a = 5   # 101
b = 3   # 011
print(a & b)   #1
#   101
#   011
#   ---
#   001
#_________________


#_________________
# bitwise OR
# اگر حداقل یکی از بیت‌ها 1 باشد، نتیجه 1 می‌شود
a = 5
b = 3
print(a | b)   #7
#   101
#   011
#   ---
#   111
#_________________


#_________________
# bitwise NOT
# تمام بیت‌ها را برعکس می‌کند
~x == -(x + 1)
a = 5
print(~a)   #-6
print(~10)  #-11
#_________________


#_________________
# bitwise XOR
# اگر بیت‌ها متفاوت باشند 1 می‌شود
a = 5   # 101
b = 3   # 011
print(a ^ b)   #6
#   101
#   011
#   ---
#   110
#_________________


#_________________
# left shift
# بیت‌ها را به چپ منتقل می‌کند
# x << n = x * (2 ** n)   برای اعداد مثبت

a = 5
print(a << 1)   #10    یعنی تمام بیت‌ها رو یک خانه به سمت چپ هل بدی
# 101 -> 1010

print(5 << 2)   #20    یعنی تمام بیت‌ها 2 خانه به سمت چپ هل بدی
# 101 -> 10100
#_________________


#_________________
# right shift
# بیت‌ها را به راست منتقل می‌کند
# x >> n = x // (2 ** n)   برای اعداد مثبت

a = 20
print(a >> 1)   #10
# 10100 -> 1010

print(20 >> 2)   #5
# 10100 -> 101
#_________________



# ------------------
# چیزی که اکثر برنامه نویس ها حفظ میکنن:
x << 1   # ضرب در 2
x << 2   # ضرب در 4

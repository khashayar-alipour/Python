#==============================
'''   Membership operators  '''
#==============================
# عملگرهای عضویت
# بررسی می‌کنند که یک مقدار داخل یک مجموعه داده (مثل لیست، رشته، تاپل و ...) وجود دارد یا نه
# نتیجه boolean میباشد


# in (وجود دارد)
numbers = [10, 20, 30, 40]
print(20 in numbers)    # True
print(50 in numbers)    # False

text = "Python"
print("y" in text)    # True

text = "I love Python"
print("Python" in text)    # True



# not in (وجود ندارد)
 # برعکس in عمل می‌کند
# اگر مقدار وجود نداشته باشد، True برمی‌گرداند

numbers = [10, 20, 30, 40]
print(50 not in numbers)    # True    چون 50 داخل لیست نیست







#===========================
'''  Identity operators  '''
#===========================
# عملگرهای هویتی
# این دو عملگر چک می‌کنند آیا دو متغیر دقیقاً به یک شیء در حافظه اشاره می‌کنند یا نه


#----- is (آیا دو متغیر دقیقاً یک شیء هستند؟) -----
a = [1, 2, 3]
b = a
print(a is b)    # True
# چون a و b به همان لیست واحد در حافظه اشاره می‌کنند

# در پایتون معمولاً از is برای None استفاده می‌شود
name = None
if name is None:
    print("No name")
if name is not None:
    print("Name exists")



#----- is not (آیا دو متغیر شیءهای متفاوتی هستند؟) -----
a = [1, 2, 3]
b = [1, 2, 3]
print(a is not b)    # True
# چون با اینکه محتوا یکسان است، دو شیء جداگانه در حافظه هستند




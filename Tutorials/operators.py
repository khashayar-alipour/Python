#===================================
'''      Logical operators      '''
#===================================
# عملگرهای منطقی


# logical and
# فقط زمانی True می‌شود که هر دو شرط درست باشند
age = 25
salary = 5000
print(age > 18 and salary > 3000)    # True

print(10 > 5 and 3 > 7)   # True  شرط دوم غلطه


# logical or
# اگر حداقل یکی از شرط‌ها درست باشد، نتیجه True می‌شود
print(10 > 5 or 3 > 7)    # True   چون شرط دوم درسته


# logical not (نقیض)
# این عملگر نتیجه همه چیز رو برعکس میکنه
print(not True)   # True

age = 15
print(not (age >= 18))   # True
# چون age >= 18 برابر False است و not آن را به True تبدیل می‌کند







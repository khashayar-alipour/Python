# این دو هم یک مدل ورودی برای توابع هستند، مثل ورودی های positional
# وقتی مشخص نیست که قراره یک تابع چندتا آرگومان ورودی داشته باشه، از args و kwargs استفاده میشه


#===== args ======

# اگر هنگام تعریف تابع کنار اسم پارامتر ورودی * بذاریم به args تبدیل میشه
# این اسم مخفف arguments هست و یعنی هر تعداد آرگومان معمولی که به تابع داده شود را جمع کن
# میتونیم هر تعداد که میخوایم آرگومان ورودی بدیم
# تایپ خروجی نهایی tuple هست

def f(*a):
    return a

f()     #() tuple
f(10)   #(10,) tuple
f(10,20,30,"a")   #(10,20,30,'a') tuple

# کاربرد رایجش در جمع کردن تعداد نامشخصی عدد هست
def add(*args):
    return sum(args)

print(add(10, 20, 30, 40))    #100




#======= keyword arguments ========

# ورودی را بصورت (key=value) میگیره

def person(**kwargs):
    print(kwargs)
person(name="Ali", age=25)     #{'name': 'Ali', 'age': 25}     <class 'dict'>


def person(**kwargs):
    print(kwargs["name"])
person(name="Ali", age=25)     #Ali




#======= unpacking ========

# * و ** فقط در تعریف تابع نیستند؛ برای باز کردن داده‌ها هم استفاده می‌شوند

# unpacking a list
numbers = [10, 20, 30]
print(*numbers)     #10 20 30

# unpacking a dictionary
data = {"name": "Ali", "age": 25}
def person(name, age):
    print(name, age)
person(**data)    #Ali  25



















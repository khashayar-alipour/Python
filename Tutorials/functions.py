# type hinting
# بوسیله این ویژگی، برای یک ورودی میتونیم یک datatype بصورت پیش فرض مشخص کنیم


# متغیرها رو میشه با اسم دقیق خودشون و با علامت (=) مقداردهی کرد
# در این روش الزامیه که حتما از اسم دقیق استفاده بشه وگرنه ارور میده

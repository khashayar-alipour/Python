#============ default argument ==============
# هنگام تعریف تابع، برای آرگومان های تابع میتونیم مقداری رو به عنوان پیش فرض درنظر بگیریم
# مقداری که به ورودی هنگام فراخوانی تابع میدیم، به مقدار پیش فرضش اولویت داره
# اگر هنگام فراخوانی تابع، به آرگومانی که مقدار پیش‌فرض داره هیچ ورودی ندیم، مقدار پیش‌فرض نمایش داده میشه

def f(a,b=0):
    print(a,b)
    
f(2,3)      #a=2 - b=3
f(2)        #a=2 - b=0
f(2,"t")    #a=2 - b='t'



# اگه تابع 2 تا آرگومان داشته باشه، میشه که به اولی مقدار default ندیم ولی به دومی بدیم
# ولی برعکسش امکان پذیر نیست. یعنی نمیشه به آرگومان اولی Default بدیم ولی به دومی ندیم
# در کل مهم نیست تعداد ورودی ها چندتاست. مهم اینه اختصاص مقدار Default از ورودی آخر به اول باشه

